public class CeilingTeacher {
    private int height;
    private int paintedColor;

    public CeilingTeacher(int height, int paintedColor) {
        this.height = height;
        this.paintedColor = paintedColor;
    }

    public int getHeight() {
        return height;
    }

    public int getPaintedColor() {
        return paintedColor;
    }
}
